function createMissionLegacyReadonlyBindings({
    getFrameMode,
    getCraftId,
}) {
    return {
        frameMode: getFrameMode,
        craftId: getCraftId,
    };
}

function createMissionLegacyStateBindings(options) {
    return {
        localStateBindings: createMissionLegacyMutableBindings(options),
        readonlyStateBindings: createMissionLegacyReadonlyBindings(options),
    };
}

