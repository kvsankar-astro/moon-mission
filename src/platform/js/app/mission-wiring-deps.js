function flattenStatePortSlices(statePort = {}) {
    return {
        ...(statePort?.app || {}),
        ...(statePort?.data || {}),
        ...(statePort?.session || {}),
        ...(statePort?.sceneView || {}),
        ...(statePort?.sceneRuntime || {}),
        ...(statePort?.interaction || {}),
        ...(statePort?.viewTransform || {}),
    };
}

function createMissionWiringContext(ports) {
    const {
        uiPort = {},
        renderPort = {},
        dataPort = {},
        clockPort = {},
        statePort = {},
    } = ports || {};

    return {
        ...uiPort,
        ...renderPort,
        ...dataPort,
        ...clockPort,
        ...flattenStatePortSlices(statePort),
    };
}

