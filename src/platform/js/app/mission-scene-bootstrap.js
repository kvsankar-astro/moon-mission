import { createAnimationSceneClass } from "./animation-scene-class.js";
import { createSceneHandlerClass } from "./scene-handler-class.js";

function createMissionSceneBootstrap(deps) {
    const {
        THREE,
        d3,
        PC,
        DEFAULT_VIEW_STATE,
        SceneHelpers,
        lunar_pole,
        bindSettingsPanel,
        initSceneHandlerDom,
        computeSVGDimensions,
        getSvgWidth,
        getSvgHeight,
        isTestMode,
        onWindowResize,
        updateCraftScale,
        getSceneHandlerRuntimeState,
        sceneCreationActions,
        sceneCameraPositionActions,
        scene3dInitActions,
        dimensionsActions,
        skyActions,
        sunActions,
        earthActions,
        moonActions,
        lunarCraterActions,
        locationActions,
        primarySecondaryBodiesActions,
        spacecraftCurveActions,
        spacecraftActions,
        lineOfSightActions,
        axesHelperActions,
        lightActions,
        sceneCameraControllerActions,
        spacecraftModelActions,
        sceneInitActions,
        orbitVectorProcessingActions,
        bodyRotationActions,
        sceneDisposeActions,
        ensureSceneViewState,
        computeSceneCameraParameters,
        adjustCameraProjectionMatrixAndSkyAngle,
        getDefaultCameraDistance,
        getBodyEphemerisState,
        resolveBodySource,
        getAnimationSceneRuntimeState,
        getViewEarthClouds,
        setViewEarthClouds,
        getViewLunarCraters,
        setViewLunarCraters,
    } = deps;

    const SceneHandler = createSceneHandlerClass({
        THREE,
        d3,
        bindSettingsPanel,
        initSceneHandlerDom,
        computeSVGDimensions,
        getSvgWidth,
        getSvgHeight,
        isTestMode,
        onWindowResize,
        updateCraftScale,
        getRuntimeState: getSceneHandlerRuntimeState,
        getViewEarthClouds,
        setViewEarthClouds,
        getViewLunarCraters,
        setViewLunarCraters,
    });

    const AnimationScene = createAnimationSceneClass({
        THREE,
        PC,
        DEFAULT_VIEW_STATE,
        SceneHelpers,
        lunar_pole,
        sceneCreationActions,
        sceneCameraPositionActions,
        scene3dInitActions,
        dimensionsActions,
        skyActions,
        sunActions,
        earthActions,
        moonActions,
        lunarCraterActions,
        locationActions,
        primarySecondaryBodiesActions,
        spacecraftCurveActions,
        spacecraftActions,
        lineOfSightActions,
        axesHelperActions,
        lightActions,
        sceneCameraControllerActions,
        spacecraftModelActions,
        sceneInitActions,
        orbitVectorProcessingActions,
        bodyRotationActions,
        sceneDisposeActions,
        ensureSceneViewState,
        computeSceneCameraParameters,
        adjustCameraProjectionMatrixAndSkyAngle,
        getDefaultCameraDistance,
        getBodyEphemerisState,
        resolveBodySource,
        getRuntimeState: getAnimationSceneRuntimeState,
    });

    return {
        SceneHandler,
        AnimationScene,
    };
}

export { createMissionSceneBootstrap };
