import { readCheckedRadioValue, toggleVisibilityById } from "../ui/dom-helpers.js";
import { createMissionRuntimeEntry } from "./mission-runtime-entry.js";
import { buildMissionRuntimeStaticDeps } from "./mission-runtime-static-deps.js";

function createMissionRuntimeWireupEntry(ctx) {
    const {
        d3,
        d3SelectAll,
        THREE,
        Astronomy,
        windowRef,
        documentRef,
        consoleRef,
        SwiperClass,
        formatMetric,
        missionStateCells,
        runtimeFlags,
        animationScenes,
        orbitDataLoaded,
        orbitDataProcessed,
        chebyshevData,
        chebyshevDataLoaded,
        npzData,
        npzDataLoaded,
        landingNpzData,
        landingNpzLoaded,
        landingChebyshevData,
        landingChebyshevLoaded,
        planetProperties,
        ephemerisRecords,
        ephemerisStatuses,
        resolveBodySource,
        getActiveEphemerisSource,
        sceneViewStateActions,
        AnimationScene,
        SceneHandlerClass,
        bridgeActions,
        modeSwitchActions,
        animation3DControllers,
        animation2DControllers,
        animationController,
        bindInfoPanelControls,
        updateEphemerisPanel,
        pixelsPerAU,
        render,
        processOrbitData,
        animateLoop,
        initAnimation,
        isRelativeMode,
        isTestMode,
    } = ctx;

    return createMissionRuntimeEntry({
        d3,
        missionStateCells,
        runtimeFlags,
        animationScenes,
        orbitDataProcessed,
        chebyshevData,
        chebyshevDataLoaded,
        npzData,
        npzDataLoaded,
        landingNpzData,
        landingNpzLoaded,
        landingChebyshevData,
        landingChebyshevLoaded,
        planetProperties,
        ephemerisStatuses,
        resolveBodySource,
        getActiveEphemerisSource,
        sceneViewStateActions,
        AnimationScene,
        bridgeActions,
        modeSwitchActions,
        staticWireupDeps: buildMissionRuntimeStaticDeps({
            d3,
            d3SelectAll,
            THREE,
            Astronomy,
            windowRef,
            documentRef,
            consoleRef,
            SwiperClass,
            formatMetric,
            animationScenes,
            animation3DControllers,
            animation2DControllers,
            orbitDataLoaded,
            orbitDataProcessed,
            chebyshevData,
            chebyshevDataLoaded,
            npzData,
            npzDataLoaded,
            ephemerisRecords,
            ephemerisStatuses,
            planetProperties,
            animationController,
            AnimationScene,
            SceneHandlerClass,
            bindInfoPanelControls,
            updateEphemerisPanel,
            PIXELS_PER_AU: pixelsPerAU,
            render,
            processOrbitData,
        }),
        readPlaneSelection: () => readCheckedRadioValue("plane", "DEFAULT"),
        toggleStatsVisibility: () => {
            toggleVisibilityById("stats");
        },
        animateLoop,
        initAnimation,
        isRelativeMode,
        isTestMode,
    });
}

export { createMissionRuntimeWireupEntry };
