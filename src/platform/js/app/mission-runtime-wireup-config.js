function buildStatePort(ctx) {
    const {
        statePorts,
        getPanXState,
        getPanYState,
        getZoomFactorState,
        setZoomFactorState,
        setPanXState,
        setPanYState,
    } = ctx;

    return {
        app: statePorts?.app || {},
        data: statePorts?.data || {},
        session: statePorts?.session || {},
        sceneView: statePorts?.sceneView || {},
        sceneRuntime: statePorts?.sceneRuntime || {},
        interaction: statePorts?.interaction || {},
        viewTransform: {
            getPanXState,
            getPanYState,
            getZoomFactorState,
            setZoomFactorState,
            setPanXState,
            setPanYState,
        },
    };
}

function buildMissionRuntimePorts(ctx) {
    return {
        uiPort: buildUiPort(ctx),
        renderPort: buildRenderPort(ctx),
        dataPort: buildDataPort(ctx),
        clockPort: buildClockPort(ctx),
        statePort: buildStatePort(ctx),
    };
}

function buildMissionRuntimeWireupConfig(ctx) {
    const sharedPorts = buildMissionRuntimePorts(ctx);

    return {
        wiringPorts: {
            ...sharedPorts,
        },
        runtimeBootstrapPorts: {
            ...sharedPorts,
        },
    };
}

export { buildMissionRuntimeWireupConfig };
