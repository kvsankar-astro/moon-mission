import { createLoadProgressController } from "./load-progress-controller.js";

function createMissionWiringComposition(ports) {
    const {
        uiPort = {},
        renderPort = {},
        dataPort = {},
        clockPort = {},
        statePort = {},
    } = ports || {};
    const ctx = {
        ...uiPort,
        ...renderPort,
        ...dataPort,
        ...clockPort,
        ...statePort,
    };

    const getStartAndEndTimes = createStartEndTimesResolver({
        getGlobalConfig: ctx.getGlobalConfig,
        getConfig: ctx.getConfig,
        createUTCTimestamp: ctx.createUTCTimestamp,
        oneMinuteMs: ctx.TC.ONE_MINUTE_MS,
    });
    const loadProgress = createLoadProgressController({
        ensureDeterminateProgressBar: ctx.ensureDeterminateProgressBar,
        setProgressBarValue: ctx.setProgressBarValue,
        showElementById: ctx.showElementById,
        hideElementById: ctx.hideElementById,
        updateProgressLabel: ctx.updateProgressLabel,
        clearProgressLabel: ctx.clearProgressLabel,
    });

    const dataflow = createDataflowWiringActions({
        d3: ctx.d3,
        THREE: ctx.THREE,
        Astronomy: ctx.Astronomy,
        sleep: ctx.sleep,
        updateProgressLabel: ctx.updateProgressLabel,
        ensureIndeterminateProgressBar: ctx.ensureIndeterminateProgressBar,
        showElementById: ctx.showElementById,
        hideElementById: ctx.hideElementById,
        loadChebyshev: ctx.loadChebyshev,
        loadNpz: ctx.loadNpz,
        processOrbitData: ctx.processOrbitData,
        resolveLandingNpzUrl: ctx.resolveLandingNpzUrl,
        resolveLandingChebyshevUrl: ctx.resolveLandingChebyshevUrl,
        getConfig: ctx.getConfig,
        getCurrentDimension: ctx.getCurrentDimension,
        setSvgContainer: ctx.setSvgContainer,
        setDataLoaded: ctx.setDataLoaded,
        setSvgX: ctx.setSvgX,
        setSvgY: ctx.setSvgY,
        setSvgWidth: ctx.setSvgWidth,
        setSvgHeight: ctx.setSvgHeight,
        setOffsetX: ctx.setOffsetX,
        setOffsetY: ctx.setOffsetY,
        getOffsetX: ctx.getOffsetX,
        getOffsetY: ctx.getOffsetY,
        animationScenes: ctx.animationScenes,
        orbitDataLoaded: ctx.orbitDataLoaded,
        chebyshevData: ctx.chebyshevData,
        chebyshevDataLoaded: ctx.chebyshevDataLoaded,
        npzData: ctx.npzData,
        npzDataLoaded: ctx.npzDataLoaded,
        getDataLoaded: ctx.getDataLoaded,
        setEventInfoText: ctx.setEventInfoText,
        getEphemerisSource: ctx.getEphemerisSource,
        getBodiesForConfig: ctx.getBodiesForConfig,
        ephemerisRecords: ctx.ephemerisRecords,
        ephemerisStatuses: ctx.ephemerisStatuses,
        updateEphemerisPanel: ctx.updateEphemerisPanel,
        getBodySource: ctx.getBodySource,
        getGlobalConfig: ctx.getGlobalConfig,
        getConfigsList: ctx.getConfigsList,
        getLandingDataLoaded: ctx.getLandingDataLoaded,
        setLandingDataLoaded: ctx.setLandingDataLoaded,
        setLandingNpzLoaded: ctx.setLandingNpzLoaded,
        setLandingNpzData: ctx.setLandingNpzData,
        setLandingChebyshevLoaded: ctx.setLandingChebyshevLoaded,
        setLandingChebyshevData: ctx.setLandingChebyshevData,
        getSvgContainer: ctx.getSvgContainer,
        planetProperties: ctx.planetProperties,
        PC: ctx.PC,
        PIXELS_PER_AU: ctx.PIXELS_PER_AU,
        getZoomFactor: ctx.getZoomFactor,
        setEpochJD: ctx.setEpochJD,
        setEpochDate: ctx.setEpochDate,
        getStartTime: ctx.getStartTime,
        getEndTimeSC: ctx.getEndTimeSC,
        getStartLandingTime: ctx.getStartLandingTime,
        getEndLandingTime: ctx.getEndLandingTime,
        getLandingNpzLoaded: ctx.getLandingNpzLoaded,
        getLandingNpzData: ctx.getLandingNpzData,
        getLandingChebyshevLoaded: ctx.getLandingChebyshevLoaded,
        getLandingChebyshevData: ctx.getLandingChebyshevData,
        getStartAndEndTimes,
        TC: ctx.TC,
        getFrameMode: ctx.getFrameMode,
        getActiveEphemerisSource: ctx.getActiveEphemerisSource,
        resolveBodySourceFn: ctx.resolveBodySourceFn,
        getBodyEphemerisRange: ctx.getBodyEphemerisRange,
        getBodyEphemerisState: ctx.getBodyEphemerisState,
        getJoyRideFlag: ctx.getJoyRideFlag,
        getLandingFlag: ctx.getLandingFlag,
        getDefaultCameraDistance: ctx.getDefaultCameraDistance,
        getAnimTime: ctx.getAnimTime,
        generateBodyCurve: ctx.generateBodyCurve,
        getLatestEndTime: ctx.getLatestEndTime,
        getPlaneVariables: ctx.getPlaneVariables,
        getOrbitStyle: ctx.getOrbitStyle,
        getTrailTrackBrightness2D: ctx.getTrailTrackBrightness2D,
        getTrailTailBrightness2D: ctx.getTrailTailBrightness2D,
        UC: ctx.UC,
        getPixelsPerAU: ctx.getPixelsPerAU,
        getEpochJD: ctx.getEpochJD,
        getEpochDate: ctx.getEpochDate,
        setEpochDisplay: ctx.setEpochDisplay,
        showPlanet: ctx.showPlanet,
        getXFactor: ctx.getXFactor,
        getYFactor: ctx.getYFactor,
        getXVariable: ctx.getXVariable,
        getYVariable: ctx.getYVariable,
        getCraftData: ctx.getCraftData,
        setZoomFactorState: ctx.setZoomFactorState,
        getPanX: ctx.getPanX,
        setPanXState: ctx.setPanXState,
        getPanY: ctx.getPanY,
        setPanYState: ctx.setPanYState,
        getPlaneSelection: ctx.getPlaneSelection,
        setPlaneVariables: ctx.setPlaneVariables,
        handleDimensionSwitch: ctx.handleDimensionSwitch,
        setLocation: ctx.setLocation,
        loadProgress,
    });

    const initConfigWiring = createInitConfigWiringActions({
        PC: ctx.PC,
        windowRef: ctx.windowRef,
        animationScenes: ctx.animationScenes,
        animation3DControllers: ctx.animation3DControllers,
        animation2DControllers: ctx.animation2DControllers,
        AnimationScene: ctx.AnimationScene,
        Animation3DController: ctx.Animation3DController,
        Animation2DController: ctx.Animation2DController,
        planetProperties: ctx.planetProperties,
        showPlanet: ctx.showPlanet,
        computeSVGDimensions: () => dataflow.svgActions.computeSVGDimensions(),
        getSvgWidth: ctx.getSvgWidth,
        getSvgHeight: ctx.getSvgHeight,
        setPixelsPerAU: ctx.setPixelsPerAU,
        setDefaultCameraDistance: ctx.setDefaultCameraDistance,
        setTrackWidth: ctx.setTrackWidth,
        setEarthRadius: ctx.setEarthRadius,
        setMoonRadius: ctx.setMoonRadius,
        getEarthRadius: ctx.getEarthRadius,
        getMoonRadius: ctx.getMoonRadius,
        setStartTime: ctx.setStartTime,
        setEndTime: ctx.setEndTime,
        setEndTimeSC: ctx.setEndTimeSC,
        setLatestEndTime: ctx.setLatestEndTime,
        setTimelineTotalSteps: ctx.setTimelineTotalSteps,
        setTicksPerAnimationStep: ctx.setTicksPerAnimationStep,
        setEpochJD: ctx.setEpochJD,
        setEpochDate: ctx.setEpochDate,
        getStartAndEndTimes,
        animationController: ctx.animationController,
        resolveOrbitUrls: ctx.resolveOrbitUrls,
        resolveOrbitMetaUrl: ctx.resolveOrbitMetaUrl,
        resolveOrbitNpzUrl: ctx.resolveOrbitNpzUrl,
        resolveOrbitSunChebyshevUrl: ctx.resolveOrbitSunChebyshevUrl,
        handleModeSwitchToGeo: ctx.handleModeSwitchToGeo,
        handleModeSwitchToLunar: ctx.handleModeSwitchToLunar,
        loadMissionConfig: ctx.loadMissionConfig,
        getGlobalConfig: ctx.getGlobalConfig,
        setGlobalConfig: ctx.setGlobalConfig,
        setEventInfos: ctx.setEventInfos,
        getEphemerisSource: ctx.getEphemerisSourceFromData,
        setEphemerisSource: ctx.setEphemerisSource,
        setBodyEphemerisSources: ctx.setBodyEphemerisSources,
        setEphemerisStatusesForConfig: ctx.setEphemerisStatusesForConfig,
        bindInfoPanelControls: ctx.bindInfoPanelControls,
        updateEphemerisPanel: ctx.updateEphemerisPanel,
        applyMissionMetadata: ctx.applyMissionMetadata,
        getPlanetProperties: ctx.getPlanetProperties,
        documentRef: ctx.documentRef,
        updateMultipleElementsText: ctx.updateMultipleElementsText,
        updateSpacecraftMnemonic: ctx.updateSpacecraftMnemonic,
        updateMoonUIFromConfig: ctx.updateMoonUIFromConfig,
        updateLandingUIFromConfig: ctx.updateLandingUIFromConfig,
        applyLandingTimesUpdate: ctx.applyLandingTimesUpdate,
        computeLandingTimesUpdate: ctx.computeLandingTimesUpdate,
        createUTCTimestamp: ctx.createUTCTimestamp,
        setStartLandingTime: ctx.setStartLandingTime,
        setEndLandingTime: ctx.setEndLandingTime,
        consoleRef: ctx.consoleRef,
        applyEventsUpdate: ctx.applyEventsUpdate,
        computeEventsUpdate: ctx.computeEventsUpdate,
        getConfig: ctx.getConfig,
        getDataEndTimeMs: (spacecraftMnemonic) => getStartAndEndTimes(spacecraftMnemonic)[1],
        computeMissionEventTimes: ctx.computeMissionEventTimes,
        setTimeTransLunarInjection: ctx.setTimeTransLunarInjection,
        setTimeLunarOrbitInsertion: ctx.setTimeLunarOrbitInsertion,
        getSceneHandler: ctx.getSceneHandler,
        setSceneHandler: ctx.setSceneHandler,
        SceneHandlerClass: ctx.SceneHandlerClass,
        d3: ctx.d3,
        getEventInfos: ctx.getEventInfos,
        bindBurnButtons: ctx.bindBurnButtons,
        getBurnButtonHandler: ctx.getBurnButtonHandler,
        SwiperClass: ctx.SwiperClass,
        loadProgress,
    });

    const { initConfig } = createInitConfigFlowActions({
        getConfig: ctx.getConfig,
        getAnimationScene: (cfg) => ctx.animationScenes[cfg],
        AnimationScene: ctx.AnimationScene,
        shouldSkipInitConfig: ctx.shouldSkipInitConfig,
        applyInitConfigAlreadyInitialized: ctx.applyInitConfigAlreadyInitialized,
        handleModeSwitchToGeo: ctx.handleModeSwitchToGeo,
        handleModeSwitchToLunar: ctx.handleModeSwitchToLunar,
        setChecked: ctx.setChecked,
        normalizePlaneSelection: ctx.normalizePlaneSelection,
        setPlaneSelectionState: ctx.setPlaneSelectionState,
        syncPlaneSelectionControls: ctx.syncPlaneSelectionControls,
        initConfigOrchestrationActions: initConfigWiring.initConfigOrchestrationActions,
        getGlobalConfig: ctx.getGlobalConfig,
        initConfigSceneSetupActions: initConfigWiring.initConfigSceneSetupActions,
        isRelativeMode: ctx.isRelativeMode,
        initConfigUiActions: initConfigWiring.initConfigUiActions,
        setSceneState: ctx.setSceneState,
        consoleRef: ctx.consoleRef,
    });

    const viewSettings = createViewSettingsWiringActions({
        d3: ctx.d3,
        getConfig: ctx.getConfig,
        animationScenes: ctx.animationScenes,
        getCurrentDimension: ctx.getCurrentDimension,
        setCurrentDimension: ctx.setCurrentDimension,
        getPreviousDimension: ctx.getPreviousDimension,
        setPreviousDimension: ctx.setPreviousDimension,
        setDimensionChanged: ctx.setDimensionChanged,
        getDimensionChanged: ctx.getDimensionChanged,
        setSvgContainer: ctx.setSvgContainer,
        initSVG: dataflow.svgActions.initSVG,
        loadOrbitDataIfNeededAndProcess: dataflow.loadOrbitDataIfNeededAndProcess,
        handleDimensionSwitch: ctx.handleDimensionSwitch,
        handlePlaneChange: dataflow.planeActions.handlePlaneChange,
        setLocation: ctx.setLocation,
        adjustLabelLocations: dataflow.adjustLabelLocations,
        getStartLandingFlag: ctx.getStartLandingFlag,
        clearStartLandingFlag: ctx.clearStartLandingFlag,
        toggleLanding: ctx.toggleLanding,
        updateProgressLabel: ctx.updateProgressLabel,
        loadProgress,
        setConfig: ctx.setConfig,
        AnimationScene: ctx.AnimationScene,
        initAnimation: ctx.initAnimation,
        readOriginMode: ctx.readOriginMode,
        readViewSettings: ctx.readViewSettings,
        setFPSCounterVisibility: ctx.setFPSCounterVisibility,
        render: ctx.render,
        getGlobalConfig: ctx.getGlobalConfig,
        setViewFlags: ctx.setViewFlags,
        onConfigChanged: ctx.onConfigChanged,
    });

    const sceneFrame = createSceneFrameWiringActions({
        d3: ctx.d3,
        formatMetric: ctx.formatMetric,
        updateEventInfo: ctx.updateEventInfo,
        clearEventInfo: ctx.clearEventInfo,
        getAnimDate: ctx.getAnimDate,
        animation2DControllers: ctx.animation2DControllers,
        animationScenes: ctx.animationScenes,
        getConfig: ctx.getConfig,
        getPlaneVariables: ctx.getPlaneVariablesStateForConfig,
        getZoomFactor: ctx.getZoomFactorStateForConfig,
        getPanX: ctx.getPanXStateForConfig,
        getPanY: ctx.getPanYStateForConfig,
        setCraftData: ctx.setCraftData,
        setLabelLocation: dataflow.setLabelLocation,
        zoomChangeTransform: dataflow.zoomChangeTransform,
        showGreenwichLongitude: dataflow.showGreenwichLongitude,
        isOrbitDataProcessed: ctx.isOrbitDataProcessed,
        getAnimTime: ctx.getAnimTime,
        computeSunLongitude: ctx.computeSunLongitude,
        computeSceneState: ctx.computeSceneState,
        getChebyshevData: ctx.getChebyshevData,
        getChebyshevDataLoaded: ctx.getChebyshevDataLoaded,
        getNpzData: ctx.getNpzData,
        getNpzDataLoaded: ctx.getNpzDataLoaded,
        getLandingNpzData: ctx.getLandingNpzDataByConfig,
        getLandingNpzLoaded: ctx.getLandingNpzLoadedByConfig,
        getLandingChebyshevData: ctx.getLandingChebyshevDataByConfig,
        getLandingChebyshevLoaded: ctx.getLandingChebyshevLoadedByConfig,
        getGlobalConfig: ctx.getGlobalConfig,
        getStartLandingTime: ctx.getStartLandingTime,
        getEndLandingTime: ctx.getEndLandingTime,
        getEventInfos: ctx.getEventInfos,
        getMissionTimes: ctx.getMissionTimes,
        getAnimationScene: (cfg) => ctx.animationScenes[cfg],
        getFrameMode: ctx.getFrameMode,
        getBodySources: ctx.getBodySources,
        getActiveEphemerisSource: ctx.getActiveEphemerisSourceForConfig,
        setSunLongitude: ctx.setSunLongitude,
        getCraftId: ctx.getCraftId,
        getPixelsPerAU: ctx.getPixelsPerAU,
        updateCraftScale: ctx.updateCraftScale,
        getCurrentDimension: ctx.getCurrentDimension,
        animation3DControllers: ctx.animation3DControllers,
        adjustCameraProjectionMatrixAndSkyAngle: ctx.adjustCameraProjectionMatrixAndSkyAngle,
        render: ctx.render,
    });

    return {
        ...dataflow,
        ...initConfigWiring,
        initConfig,
        ...viewSettings,
        ...sceneFrame,
        getStartAndEndTimes,
    };
}

export { createMissionWiringComposition };
