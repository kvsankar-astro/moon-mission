function createMissionRuntimeHandlersEntryContext({
    performanceRef,
    requestAnimationFrameRef,
    startMissionApp,
    eventBus,
    toggleModeGuarded,
    toggleRelativeMode,
    getStartupAnimTimeOverride,
    runtimeLoopState,
    getFpsUpdateInterval,
    getTicksPerAnimationStep,
    updateFPSCounter,
    updateFpsCounterState,
    updateFrameDeltaState,
    computeAnimationStepState,
    animationController,
    animationScenes,
    runtimeViewState,
    bridgeActions,
    updateThreeDLoopCamera,
}) {
    return {
        performanceRef,
        requestAnimationFrameRef,
        startMissionApp,
        eventBus,
        toggleModeGuarded,
        toggleRelativeMode,
        getStartupAnimTimeOverride,
        readLoopState: () => runtimeLoopState.getLoopState(),
        writeLoopState: (nextLoopState) => {
            runtimeLoopState.setLoopState(nextLoopState);
        },
        getFpsUpdateInterval,
        getTicksPerAnimationStep,
        updateFPSCounter,
        updateFpsCounterState,
        updateFrameDeltaState,
        computeAnimationStepState,
        getAnimationController: () => animationController,
        getScene: () => animationScenes[runtimeViewState.getConfig()],
        getCameraControlsCallback: () => bridgeActions.cameraControlsCallback,
        updateThreeDLoopCamera,
    };
}

